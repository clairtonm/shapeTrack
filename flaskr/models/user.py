class User:
    def __init__(self, user_id, username, created_at, birthdate, city, country, state, height, sex, email):
        self.user_id = user_id
        self.username = username
        self.created_at = created_at
        self.birthdate = birthdate
        self.city = city
        self.country = country
        self.state = state
        self.height = height
        self.sex = sex
        self.email = email